class Food {
  final String name; // name of food
  final String weight;
  final int servings;
  final int calories;
  final int protein;
  final int carb;
  final int fat;

  Food({
    required this.name,
    required this.weight,
    required this.servings,
    required this.calories,
    required this.protein,
    required this.carb,
    required this.fat
  });
}
